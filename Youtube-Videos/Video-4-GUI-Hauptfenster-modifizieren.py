import tkinter as tk

root = tk.Tk()
root.title("Hier steht der Titel")
root.geometry("800x400") # legt eine Größe fest die aber veränderbar ist
root.minsize(width=250, height=250) # legt die minimalgröße des Fensters fest
root.maxsize(width=600, height=600) # legt die maximalgröße des Fensters fest
root.resizable(width=False, height=False) # die Größe ist nun nicht mehr veränderbar geht auch mit True

label1 = tk.Label(root, text="Hallo Welt")
label1.pack()

root.mainloop()

